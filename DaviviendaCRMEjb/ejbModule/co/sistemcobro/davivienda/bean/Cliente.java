package co.sistemcobro.davivienda.bean;

import java.util.ArrayList;
import java.util.List;

public class Cliente {
	
	private String identificacion;
	private String nombre;
	private String idcliente;
	private String ciudad;
	private String direccion;
	private String Correo;
	private String tipoCartera;
	private String oficina;
	private String casa;
	private List<Obligacion> obligaciones;
	private List<ObligacionHoy> obligacionesHoy;
	private Obligacion obligacion;
	
	public void setObligacionesHoy(List<ObligacionHoy> obligacionesHoy) {
		this.obligacionesHoy = obligacionesHoy;
	}
	private List<Telefono> telefonos;
	
	
	
	public String getIdentificacion() {
		return identificacion;
	}
	public void setIdentificacion(String identificacion) {
		this.identificacion = identificacion;
	}
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public String getIdcliente() {
		return idcliente;
	}
	public void setIdcliente(String idcliente) {
		this.idcliente = idcliente;
	}
	public String getCiudad() {
		return ciudad;
	}
	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}
	public String getDireccion() {
		return direccion;
	}
	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}
	public String getCorreo() {
		return Correo;
	}
	public void setCorreo(String correo) {
		Correo = correo;
	}
	public String getTipoCartera() {
		return tipoCartera;
	}
	public void setTipoCartera(String tipoCartera) {
		this.tipoCartera = tipoCartera;
	}
	public String getOficina() {
		return oficina;
	}
	public void setOficina(String oficina) {
		this.oficina = oficina;
	}
	public String getCasa() {
		return casa;
	}
	public void setCasa(String casa) {
		this.casa = casa;
	}
	public List<Obligacion> getObligaciones() {
		return obligaciones;
	}
	public void setObligaciones(List<Obligacion> obligaciones) {
		this.obligaciones = obligaciones;
	}
	public List<Telefono> getTelefonos() {
		return telefonos;
	}
	public void setTelefonos(List<Telefono> telefonos) {
		this.telefonos = telefonos;
	}
	public List<ObligacionHoy> getObligacionesHoy() {
		return obligacionesHoy;
	}
	public void setObligacionesHoy(ArrayList<ObligacionHoy> obligacionesHoy) {
		this.obligacionesHoy = obligacionesHoy;
	}
	public Obligacion getObligacion() {
		return obligacion;
	}
	public void setObligacion(Obligacion obligacion) {
		this.obligacion = obligacion;
	}
	
	
		

}
