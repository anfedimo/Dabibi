package co.sistemcobro.davivienda.bean;

import java.sql.Timestamp;

public class Telefono {
	private String idcliente;
	private String tipo;
	private String telefono;
	private Integer idTelefonoRetiro;

	private String identificacion;
	private String etiqueta;
	
	private String probabilidad;
	private Integer cantLocalizado;
	private Integer cantILocalizado;
	
	private Timestamp fechaRetiro;
	private String motivoRetiro;
	private Integer idCargue;
		
	
	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getTelefono() {
		return telefono;
	}

	public void setTelefono(String telefono) {
		this.telefono = telefono;
	}

	public String getIdcliente() {
		return idcliente;
	}

	public void setIdcliente(String idcliente) {
		this.idcliente = idcliente;
	}

	public String getIdentificacion() {
		return identificacion;
	}

	public void setIdentificacion(String identificacion) {
		this.identificacion = identificacion;
	}

	public String getEtiqueta() {
		return etiqueta;
	}

	public void setEtiqueta(String etiqueta) {
		this.etiqueta = etiqueta;
	}

	public String getProbabilidad() {
		return probabilidad;
	}

	public void setProbabilidad(String probabilidad) {
		this.probabilidad = probabilidad;
	}

	public Integer getCantLocalizado() {
		return cantLocalizado;
	}

	public void setCantLocalizado(Integer cantLocalizado) {
		this.cantLocalizado = cantLocalizado;
	}

	public Integer getCantILocalizado() {
		return cantILocalizado;
	}

	public void setCantILocalizado(Integer cantILocalizado) {
		this.cantILocalizado = cantILocalizado;
	}

	public Integer getIdTelefonoRetiro() {
		return idTelefonoRetiro;
	}

	public void setIdTelefonoRetiro(Integer idTelefonoRetiro) {
		this.idTelefonoRetiro = idTelefonoRetiro;
	}

	public Timestamp getFechaRetiro() {
		return fechaRetiro;
	}

	public void setFechaRetiro(Timestamp fechaRetiro) {
		this.fechaRetiro = fechaRetiro;
	}

	public String getMotivoRetiro() {
		return motivoRetiro;
	}

	public void setMotivoRetiro(String motivoRetiro) {
		this.motivoRetiro = motivoRetiro;
	}

	public Integer getIdCargue() {
		return idCargue;
	}

	public void setIdCargue(Integer idCargue) {
		this.idCargue = idCargue;
	}

	
	
	
	
	

}
